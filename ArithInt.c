void main() {
    int x, y;

    x = 100;
    y = 10;

    printf("X= ");
    printf(x);
    printf("\n");
    printf("Y= ");
    printf(y);
    printf("\n");

    printf("Add: ");
    printf(x + y);
    printf("\n");
    printf("Sub: ");
    printf(x - y);
    printf("\n");
    printf("Mul: ");
    printf(x * y);
    printf("\n");
    printf("Div: ");
    printf(x / y);
    printf("\n");
}